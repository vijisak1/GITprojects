package week1.day1;

public class Switch {
	public static void main(String[] args) {
		
		int status = 2;
		String grade = null;
		switch(status) {
		case 1:
			grade = "passed";
			 break;
		case 2:
			grade = "failed";

			break;
		}
		System.out.println(grade);
		
			
			
			
		
	}
	

}
